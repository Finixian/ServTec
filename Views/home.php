<p><strong><span style="color: #1E90FF;">4) Requisitos para abrir expediente de Servicio Social:</span></strong></p>
<p>Para iniciar el tr&aacute;mite de apertura de expediente de Servicio Social, los requisitos se enlistan a continuaci&oacute;n:</p>
<ul style="list-style-type: square;">
<li>Deber&aacute; haber seleccionado un programa, debidamente avalado por la dependencia u organismo donde pretenda realizar su Servicio Social y por el <strong>Departamento de Gesti&oacute;n Tecnol&oacute;gica y Vinculaci&oacute;n </strong>del ITT.</li>
<li>Para realizar el Servicio Social fuera del ITT usted deber&aacute; contar con el <strong>70% de los cr&eacute;ditos cursados y aprobados o que al momento de cursar su semestre sume 70% de los cr&eacute;ditos con las materias que est&aacute; cursando</strong>, en caso contrario, si opta por realizar dicho servicio dentro del ITT el porcentaje m&iacute;nimo es del 65%. Adem&aacute;s, en ambos casos, tendr&aacute; que haber acreditado las actividades extraescolares y contar con sus 5 cr&eacute;ditos complementarios.</li>
<li>Deber&aacute; presentarse en la dependencia u organismo con una carta de presentaci&oacute;n expedida por el <strong>Departamento de Gesti&oacute;n Tecnol&oacute;gica y Vinculaci&oacute;n</strong>.</li>
<li>Deber&aacute; cubrir un <strong>m&iacute;nimo de 480 horas </strong>y un <strong>m&aacute;ximo de 500 horas</strong> de acuerdo con las caracter&iacute;sticas del programa de trabajo, en un per&iacute;odo no menor de <strong>6 meses</strong>, ni mayor a <strong>2 a&ntilde;os</strong>.</li>
<li>Disponer de 4 horas libres diariamente para asistir al Servicio Social.</li>
<li>Estar legalmente inscrito en el ITT.</li>
<li>Haber realizado el curso de inducci&oacute;n al Servicio Social y obtener una puntuaci&oacute;n aprobatoria en el examen que acredite que usted esta capacitado para comenzar con esta actividad.</li>
</ul>





