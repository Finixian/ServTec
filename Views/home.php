	  <p>Se entiende por Servicio Social el trabajo de car&aacute;cter temporal y obligatorio, que institucionalmente prestan y ejecutan los estudiantes en beneficio de la sociedad. Este tiene un valor curricular equivalente a 10 cr&eacute;ditos, los cuales ser&aacute;n evaluados por la dependencia donde presten el servicio social y por la (OSSDC).</p>
<p></p>
<p><strong><span style="color: #1E90FF;">1) Objetivos del Servicio Social:</span></strong></p>
<ol>
<li>Convertir esta prestaci&oacute;n en un acto de reciprocidad para con la sociedad a trav&eacute;s de los planes y programas del sector p&uacute;blico.</li>
<li>Contribuir a la formaci&oacute;n acad&eacute;mica y capacitaci&oacute;n profesional del prestador del Servicio Social.</li>
<li>Desarrollar en el prestador una conciencia de solidaridad y compromiso con la sociedad a la que pertenece.</li>
</ol>
<p><strong></strong></p>
<p><strong><span style="color: #1E90FF;">2) Prestaci&oacute;n de Servicio Social:</span></strong></p>
<p>La prestaci&oacute;n del Servicio Social puede realizarse en dependencias p&uacute;blicas, gubernamentales, no gubernamentales, organismos privados que cuenten con programas de asistencia social y desarrollo comunitario establecidos en el Plan Nacional de Desarrollo 2013 &ndash; 2018.</p>
<p><strong>Los programas de Servicio Social podr&aacute;n ser de:</strong></p>
<ul style="list-style-type: square; text-align: justify;">
<li>Educaci&oacute;n para adultos.</li>
<li>Programas de contingencia.</li>
<li>Cuidado al medio ambiente y desarrollo sustentable.</li>
<li>Apoyo a la salud.</li>
<li>Promotores y/o instructores de actividades culturales, deportivas y c&iacute;vicas.</li>
<li>Operaci&oacute;n de programas especiales gubernamentales.</li>
<li>Programas productivos en beneficio social.</li>
<li>Programas dentro del instituto con acci&oacute;n social.</li>
<li>Cualquier programa establecido espec&iacute;ficamente por el instituto tecnol&oacute;gico que est&eacute;n relacionados con las acciones antes mencionadas.</li>
</ul>
<p><strong></strong></p>
